#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char	*ft_strcpy(char *dest, char	*src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	get_lenght(char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*dest;
	char	*ptr;

	ptr = ((dest = (char *)malloc(get_lenght(src) * sizeof(char) + 1)));
	if (!ptr)
		return (0);
	ft_strcpy(dest, src);
	return (dest);
}

